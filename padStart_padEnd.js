/**
 * padStart() is a string method used to pad the start of a string with a specific
 * string to a certain length
 * */

let text = "Youtube tutorial"


console.log(text.padStart(50,'r'))
console.log(text.padStart(50,'r').length)

console.log(text.padEnd(40,"a"))
console.log(text.padEnd(40,"a").length)